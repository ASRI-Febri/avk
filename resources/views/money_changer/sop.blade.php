    <td>UV Light, Counterfeit Pen, Microscope</td>
    <td>Lapor otoritas jika terdeteksi palsu</td>
  </tr>
  <tr>
    <td>Hitung Transaksi</td>
    <td>Gunakan rumus: <br>Jual: Valas × Kurs Jual <br>Beli: Rupiah ÷ Kurs Beli</td>
    <td>Kalkulator khusus, Software MC</td>
    <td>Double-check perhitungan</td>
  </tr>
  <tr>
    <td>Pencatatan</td>
    <td>Input ke sistem, cetak bukti transaksi</td>
    <td>Software Money Changer, Printer</td>
    <td>Arsipkan copy fisik & digital</td>
  </tr>
</table>

