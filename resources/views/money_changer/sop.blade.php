<!-- Bagian 1 -->
<h2>1. Tujuan dan Ruang Lingkup</h2>
<p><strong>1.1 Tujuan:</strong></p>
<ul>
  <li>Memastikan transaksi valuta asing akurat dan sesuai regulasi</li>
  <li>Mencegah money laundering dan pendanaan terorisme</li>
  <li>Menjamin konsistensi pelayanan kepada pelanggan</li>
</ul>

<p><strong>1.2 Ruang Lingkup:</strong></p>
<ul>
  <li>Transaksi jual/beli valas tunai</li>
  <li>Verifikasi identitas pelanggan</li>
  <li>Manajemen risiko pemalsuan uang</li>
  <li>Prosedur rekonsiliasi harian</li>
</ul>

<!-- Bagian 2 -->
<h2>2. Prosedur Operasional Transaksi</h2>
<table>
  <tr>
    <th width="15%">Langkah</th>
    <th width="35%">Prosedur</th>
    <th width="25%">Alat/Peralatan</th>
    <th width="25%">Catatan</th>
  </tr>
  <tr>
    <td>Penyambutan</td>
    <td>Sapa pelanggan, tanyakan jenis transaksi</td>
    <td>Papan kurs digital</td>
    <td>Update kurs setiap 30 menit</td>
  </tr>
  <tr>
    <td>Verifikasi ID</td>
    <td>Untuk transaksi ≥ Rp 50 juta: scan KTP/paspor</td>
    <td>Formulir CDD, Scanner</td>
