<!-- Bagian 4 -->
<h2>4. Protokol Verifikasi dan Keamanan</h2>
<h3>4.1. Customer Due Diligence (CDD)</h3>
<ul>
  <li>Wajib untuk transaksi ≥ Rp 50.000.000</li>
  <li>Formulir berisi: Nama lengkap, no ID, alamat, tujuan transaksi</li>
  <li>Simpan dokumen minimum 5 tahun</li>
</ul>

<h3>4.2. Deteksi Uang Palsu</h3>
<table>
  <tr>
    <th>Alat</th>
    <th>Fungsi</th>
    <th>Indikator Keaslian</th>
  </tr>
  <tr>
    <td>UV Light</td>
    <td>Deteksi watermark & benang pengaman</td>
    <td>Garis fluoresens tampak jelas</td>
  </tr>
  <tr>
    <td>Counterfeit Pen</td>
    <td>Uji kandungan pati kertas</td>
    <td>Garis kuning (asli), hitam (palsu)</td>
  </tr>
  <tr>
    <td>Microscope</td>
    <td>Periksa microprinting</td>
    <td>Teks kecil terbaca jelas</td>
  </tr>
</table>

<!-- Bagian 5 -->
<h2>5. Rekonsiliasi dan Pelaporan</h2>
<p><strong>5.1 Prosedur Harian:</strong></p>
<ul>
  <li>Hitung fisik kas sebelum dan sesudah operasional</li>
  <li>Bandingkan dengan catatan sistem</li>
  <li>Buat laporan: Total transaksi, selisih kurs, saldo akhir</li>
</ul>

<p><strong>5.2 Pelaporan Regulator:</strong></p>
<ul>
  <li>Laporan bulanan ke Bank Indonesia</li>
  <li>Pelaporan transaksi mencurigakan ke PPATK</li>
  <li>Audit internal triwulanan</li>
</ul>

